class EmployeeData {
  late final String uid;
  late final String name;
  late final String address;
  late final String email;
  late final String phone;
  late final String dateOfBirth;
  late final String gender;
  late final String maritalStatus;
  late final String branch;
  late final String department;
  late final String supervisor;
  late final String empType;
  late final String joiningDate;
  late final String officeTime;
  late final String bankName;
  late final String bankAccNum;
  late final String accHolderName;
  late final String accType;

  EmployeeData({required this.uid,
    required this.name, required this.address, required this.email, required this.phone,
    required this.dateOfBirth, required this.gender, required this.maritalStatus, required this.branch,
    required this.department, required this.supervisor, required this.empType, required this.joiningDate,
    required this.officeTime, required this.bankName, required this.bankAccNum, required this.accHolderName,
    required this.accType});
}
